export enum Pieces {
  PECAS_DE_COBRE = 'PC',
  PECAS_DE_PRATA = 'PP',
  PECAS_DE_ELECTRUM = 'PE',
  PECAS_DE_OURO = 'PO',
  PECAS_DE_PLATINA = 'PL',
}

// Peças de cobre(PC):

// As peças de cobre(PC) é uma das moedas comuns em Dungeons and dragons, porém
// é a de menor valor, normalmente usada em "troco" quando você paga um valor em
// prata acima do preço, além disso, 10 peças de cobre, equivalem a 1 peça de prata(pp),
// essas duas moedas são mais comuns entre "pessoas normais", como uns npc's figurantes.

// peças de prata(PP):

// As peças de prata(pp) normalmente os mestres não utilizam muito para dar aos
// aventureiros, por esse motivo é bem mais normal com os npc's. 10 peças de prata(pp)
// equivalem a 1 peça de ouro, que é a moeda mais comum usada por mercadores e por
// aventureiros, e novamente como as peças de cobre(pc), as peças de prata(pp)
// também são mais utilizadas como troco.

// Peças de electrum (PE):

// Apesar de valer menos que o ouro, ela é bem mais rara e menos utilizada do que
// as outras moedas, que às vezes, as pessoas só vão conseguir vendendo artefatos de
// arte do livro, que alguns valem peças de electrum(PE), as peças de electrum são uma
// liga de ouro e prata, e uma peça de electrum vale metade de uma peça de ouro(PO)
// no caso, vale 5 pratas.

// Peças de ouro(PO):

// As peças de ouro são as mais comuns entre os aventureiros, na maioria das
// campanhas, quando eles são mandado pra uma quest, se a recompensa for em dinheiro,
// provavelmente será em peças de ouro(PO), também pode ser platina(PL), só que é bem
// mais incomum do que ouro, pelo motivo de valer bem mais que os outros. 100 peças
// de ouro valem uma platina.

// Peças de platina(PL):

// As peças de platina(PL) são as moeda mais valiosas do jogo, valendo cada 100 ouros,
// essas moedas por serem muito valiosas, são bem raras no jogo, em caso de npc's e etc,
// pois até que é bem comum para aventureiros, porque caso você for comprar algo que vale
// 100 ouros(por exemplo) você pode simplesmente pagar com uma platina, mas lembrando,
// peças de platina, normalmente sãos comuns para aventureiros, e pessoas da realeza.
